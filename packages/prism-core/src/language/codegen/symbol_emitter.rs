//! Concrete `SymbolEmitter` implementations вЂ” TS, C#, EmmyDoc, GDScript.
//!
//! Port of `language/codegen/symbol-emitter.ts`. The TS version used
//! a `SymbolEmitter` base class with protected override points; the
//! Rust port uses plain structs that implement [`Emitter`] directly
//! and share pattern via private free functions. No class hierarchy,
//! no dynamic dispatch between subclasses вЂ” each emitter owns its
//! whole format.
//!
//! Every emitter accepts `Vec<SymbolDef>` via the shared
//! [`EMITTER_KIND_SYMBOLS`](super::types::EMITTER_KIND_SYMBOLS) slot
//! on [`CodegenInputs`](super::types::CodegenInputs).

use super::source_builder::SourceBuilder;
use super::symbol_def::{SymbolDef, SymbolKind, SymbolParam};
use super::types::{CodegenMeta, CodegenResult, EmittedFile, Emitter, EMITTER_KIND_SYMBOLS};
use crate::language::syntax::case_utils::{
    to_camel_case, to_pascal_case, to_screaming_snake, to_snake_case,
};
use serde_json::Value as JsonValue;

/// Rename hook applied to every symbol name. Lets the same
/// [`SymbolDef`] tree render as `camelCase` TS, `PascalCase` C#, and
/// `SCREAMING_SNAKE_CASE` GDScript constants without mutating the
/// source of truth.
pub type NameTransform = fn(&str, SymbolKind) -> String;

fn transform(name: &str, kind: SymbolKind, t: Option<NameTransform>) -> String {
    match t {
        Some(f) => f(name, kind),
        None => name.to_owned(),
    }
}

fn downcast_symbols(input: &dyn std::any::Any) -> Option<&Vec<SymbolDef>> {
    input.downcast_ref::<Vec<SymbolDef>>()
}

fn js_literal(value: Option<&JsonValue>) -> String {
    match value {
        Some(JsonValue::String(s)) => format!("'{s}'"),
        Some(v) => v.to_string(),
        None => "undefined".into(),
    }
}

fn param_list(params: &[SymbolParam]) -> String {
    params
        .iter()
        .map(|p| {
            format!(
                "{}{}: {}",
                p.name,
                if p.optional { "?" } else { "" },
                p.r#type
            )
        })
        .collect::<Vec<_>>()
        .join(", ")
}

